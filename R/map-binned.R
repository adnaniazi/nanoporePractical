                     ')'= '\U1F922',
                     '*'= '\U1F922',
                     #10–19 15
                     '+'= '\U1F915' ,
                     ','= '\U1F915' ,
                     '-'= '\U1F915' ,
                     '.'= '\U1F915' ,
                     '/'= '\U1F915' ,
                     '0'= '\U1F915' ,
                     '1'= '\U1F915' ,
                     '2'= '\U1F915' ,
                     '3'= '\U1F915' ,
                     '4'= '\U1F915' ,
                     #20–24 22
                     '5'= '\U1F642',
                     '6'= '\U1F642',
                     '7'= '\U1F642',
                     '8'= '\U1F642',
                     '9'= '\U1F642',
                     #25–29 27
                     '='= '\U1F601',
                     ':'= '\U1F601',
                     ';'= '\U1F601',
                     '<'= '\U1F601',
                     '='= '\U1F601',
                     '>'= '\U1F601',
                     #30–34 33
                     '?'= '\U1F618',
                     '@'= '\U1F618',
                     'A'= '\U1F618',
                     'B'= '\U1F618',
                     'C'= '\U1F618',
                     #35–39 37
                     'D'= '\U1F60D',
                     'E'= '\U1F60D',
                     'F'= '\U1F60D',
                     'G'= '\U1F60D',
                     'H'= '\U1F60D',
                     #≥ 40 40
                     'I'= '\U1F929',
                     'J'= '\U1F929')

  emoji_string = ''
  for (i in seq(nchar(quality_score_string))) {
    emoji_string <- paste0(emoji_string, binned_map[substr(quality_score_string, i, i)])
  }
  return(emoji_string)
}

